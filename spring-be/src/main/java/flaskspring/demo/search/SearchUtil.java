    public String containCityQuery(String searchQuery) {
        String originalCity = getOriginalCityName(searchQuery); // 줄인 말 원래로 복원
        for (String cityQuery : CITY_SEARCH_QUERIES) {
            if (originalCity.toLowerCase().contains(cityQuery.toLowerCase())) { // 검색어에 해당 지역명이 포함되어 있는지 확인 (대소문자 무시)
                return cityQuery; // city 검색어 포함한 경우 city 리턴
            }

        }
        return null; // 없으면 null 리턴
    }

