    public String removeCityQuery(String searchQuery) {
        String cleanedQuery = searchQuery;
        // 모든 지역명 검색어에 대해 반복
        for (String cityQuery : CITY_SEARCH_QUERIES) {
            // 검색어에 해당 지역명이 포함되어 있는지 확인
            if (searchQuery.toLowerCase().contains(cityQuery.toLowerCase())) {
                // 포함된 경우 해당 지역명 부분을 제거하고 trimmed 후 반환
                cleanedQuery = searchQuery.replaceAll("(?i)" + cityQuery, "").trim();
                break;
            }
        }
        return cleanedQuery;
    }

    public String containRegionQuery(String searchQuery) {
        // 검색어가 시/군/구 포함 여부 확인
        for (String regionQuery : REGION_SEARCH_QUERIES) {
            // 검색어에 해당 지역명이 포함되어 있는지 확인 (대소문자 무시)
            if (searchQuery.toLowerCase().contains(regionQuery.toLowerCase())) {
                return regionQuery; // region 검색어 포함한 경우 region 리턴
            }
        }
        return null; // 없으면 null 리턴
    }

